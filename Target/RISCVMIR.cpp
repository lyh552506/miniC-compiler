RISCVBasicBlock::RISCVBasicBlock(std::string _name):NamedMOperand(_name){}

RISCVFunction::RISCVFunction(Function* _func):RISCVGlobalObject(_func->GetType(),_func->GetName()),func(_func){}

void RISCVBasicBlock::printfull(){
    NamedMOperand::print();
    std::cout<<":\n\t";
    for(auto minst:*this)
        minst->printfull();
}

